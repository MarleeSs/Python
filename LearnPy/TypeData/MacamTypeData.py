#Tipe Data INTEGER
angka = 10
print("Angka", angka, "Adalah Tipe", type(angka)) #Angka bilangan Bulat adalah Integer

#Tipe Data Float
angka = 10.0
print("Angka", angka, "Adalah Tipe", type(angka)) #Angka bilangan Desimal adalah Integer

#Tipe Data BOOLEAN
angka = 9
angka_boolean = bool(angka) #Angka Boolean adalah 0 dan 1
print("Angka", angka,"Adalah", angka_boolean, type(angka_boolean))#Jika 0, berarti False. Jika 1, berarti True

#Tipe Data STRING
Data = "Marli Ganteng"
Data_String = str(Data)
print("Huruf/Angka", Data, "Adalah Tipe Data", type(Data_String))#Bisa Angka dan juga Huruf